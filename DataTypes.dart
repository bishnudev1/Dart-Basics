void main(){

  // Static Datatypes
      // Integer Number
      int a = 10;
      // Double Number
      double b = 10.5;
      // Boolean Number
      bool c = true;
      // String
      String name = "Bishnudev";

    print(a,b,c,name);

  // Dynamic Datatypes
      // Var -> Can't change it's type runtime
      var value = 10;
      print(var);

      // Dynamic -> Can change it's type runtime
      dynamic data = 10;
      print(data);

  //  Final Datatype
      String x = "Bishnudev";
      final name = x;
      // Here x is changeble but not name
      print(name);

      // Const Datatype
      const bool isTrue = false;
      print(isTrue);
}